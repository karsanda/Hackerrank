import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int test = sc.nextInt();
        
        for(int i=0; i<test; i++){
            int n = sc.nextInt();
            System.out.println(n+1);
        }
    }
}